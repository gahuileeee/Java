package test1;

public class Test03 {
	/*
	 * 날짜 : 2023-12-28
	 * 이름 : 이가희
	 * 내용 : 자바 연산자 연습문제
	 */
	public static void main(String[] args) {
		int num=1;
		int result;
		result = num++;
		System.out.println("result : "+result);
		
		result = ++num;
		System.out.println("result : "+ result);
		
		result = num--;
		System.out.println("result : "+ result);
		
		result = --num;
		System.out.println("result : "+ result);
	}

}
